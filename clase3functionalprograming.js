//10- Implement the filter Method on a Prototype
Array.prototype.myFilter = function (callback) {
  const newArray = [];
  for (const [index, elem] of this.entries()) {
    if (callback(elem, index, this)) newArray.push(elem);
  }
  return newArray;
};

